// Prompt: Target Audience - General Public
// Returns the full prompt as a string
export function generalPublicAudiencePrompt(): string {
   return `# Target Audience: General Public

**Psychological Profile:**

- **Diverse backgrounds:** Wide range of prior knowledge and interests
- **Attention economy:** Easily distracted, low tolerance for jargon
- **Trust factors:** Skeptical of hype, values transparency and authenticity
- **Motivations:** Practical value, entertainment, social relevance
- **Barriers:** Information overload, lack of context, time constraints

**Engagement Context:**

- **Entry points:** Social media, news aggregators, word of mouth
- **Consumption habits:** Skimming, scanning, sharing bite-sized insights
- **Trust signals:** Cites reputable sources, avoids sensationalism
- **Emotional triggers:** Humor, surprise, relatability
- **Accessibility:** Needs clear explanations, visual aids, analogies

**Language Patterns & Voice:**

- **Plain language:** Avoids technical terms unless defined
- **Conversational tone:** "Imagine if...", "You might be wondering..."
- **Inclusive phrasing:** "Anyone can...", "No experience required"
- **Storytelling:** Uses anecdotes, real-world examples
- **Visual cues:** References to images, infographics, or videos

**Content Tactics:**

- **Hook strategy:** Start with a surprising fact or question
- **Brevity:** Short paragraphs, bullet points, bolded key ideas
- **Shareability:** Memorable phrases, quotable lines
- **Contextualization:** Relates topic to current events or trends
- **Call to action:** Encourages further exploration or sharing

**Quality Benchmarks:**

- No unexplained jargon or acronyms
- At least one real-world analogy or story
- Cites at least one reputable source
- Visual or structural aid for complex ideas
- Clear, actionable takeaway for the reader
`;
}
